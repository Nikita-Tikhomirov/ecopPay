<script>
    document.addEventListener('DOMContentLoaded', () => {

        const COOKIE_NAME = 'cart_courses'

        function getCart() {
            const cookie = document.cookie
                .split('; ')
                .find(row => row.startsWith(COOKIE_NAME + '='))

            if (!cookie) return []

            const cart = cookie.split('=')[1].split(',')
            // Фильтруем пустые значения как в order.php
            return cart.filter(id => id && id.trim() !== '')
        }

        function saveCart(ids) {
            document.cookie =
                COOKIE_NAME + '=' + ids.join(',') +
                '; path=/; max-age=' + (60 * 60 * 24 * 30)
        }

        function addToCart(productId) {
            let cart = getCart()
            const normalizedId = decodeURIComponent(String(productId))

            if (!cart.includes(normalizedId)) {
                cart.push(normalizedId)
                saveCart(cart)
                updateCartWidget()
            }
        }

        function updateCartWidget() {
            const cart = getCart()
            const countEl = document.querySelector('.cartCount')
            const widget = document.querySelector('.cartWidget')

            if (cart.length > 0 && widget && countEl) {
                countEl.innerText = cart.length
                // Показываем виджет если он был скрыт
                widget.style.display = 'flex'
            } else if (widget) {
                // Скрываем виджет если корзина пуста
                widget.style.display = 'none'
            }
        }

        // ======================
        // КНОПКИ КОРЗИНЫ
        // ======================

        document.querySelectorAll('.cartBtnsWrap').forEach(wrap => {

            const productId = wrap.dataset.productId

            const cartBtn = wrap.querySelector('.cartBtn')
            const cartLink = wrap.querySelector('.cartLink')

            if (cartBtn) {
                cartBtn.addEventListener('click', function () {
                    addToCart(productId)
                    alert('Курс добавлен в корзину')
                })
            }

            if (cartLink) {
                cartLink.addEventListener('click', function (e) {
                    e.preventDefault()

                    addToCart(productId)

                    // сохраняем куда идти
                    sessionStorage.setItem('redirect_after_cart', this.href)

                    // ПЕРЕЗАГРУЖАЕМ страницу (вот ключ!)
                    window.location.reload()
                })
            }

        })

    })

    document.addEventListener('DOMContentLoaded', () => {
        const redirect = sessionStorage.getItem('redirect_after_cart')

        if (redirect) {
            sessionStorage.removeItem('redirect_after_cart')
            window.location.href = redirect
        }
    })
</script>

