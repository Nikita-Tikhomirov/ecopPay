<script>
    document.addEventListener('DOMContentLoaded', () => {
        const customPopupCloseBtn = document.querySelectorAll('.newCustomPopup__close');
        const customPopupBg = document.querySelector('.newCustomPopupDiscount');
        const customPopupBg2 = document.querySelector('.newCustomPopupSearch');

        customPopupCloseBtn.forEach(element => {
            element.addEventListener('click', () => {
                customPopupBg.classList.remove('active');
                customPopupBg2.classList.remove('active');
            });
        });

        function showDiscountPopup() {
            customPopupBg.classList.add('active');
        }

        // Установка логики отображения попапа
        function initPopupLogic() {
            const popupData = JSON.parse(localStorage.getItem('popupData')) || {
                shownCount: 0,
                firstVisit: Date.now()
            };

            // Если уже показали 2 раза — больше не показываем
            if (popupData.shownCount >= 2) return;

            // Если первый раз — показываем через 15 сек
            if (popupData.shownCount === 0) {
                setTimeout(() => {
                    showDiscountPopup();
                    popupData.shownCount++;
                    localStorage.setItem('popupData', JSON.stringify(popupData));

                    // После закрытия или через 90 сек — показать второй раз
                    setTimeout(() => {
                        const updatedData = JSON.parse(localStorage.getItem('popupData')) || popupData;
                        if (updatedData.shownCount < 2) {
                            showDiscountPopup();
                            updatedData.shownCount++;
                            localStorage.setItem('popupData', JSON.stringify(updatedData));
                        }
                    }, 90000); // 90 секунд
                }, 15000); // 15 секунд
            } else if (popupData.shownCount === 1) {
                // Если был показан 1 раз, но страница перезагружена — подождем 90 сек и покажем еще раз
                setTimeout(() => {
                    showDiscountPopup();
                    popupData.shownCount++;
                    localStorage.setItem('popupData', JSON.stringify(popupData));
                }, 90000);
            }
        }

        initPopupLogic();
    });
</script>




